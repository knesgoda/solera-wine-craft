## What Is the OW-1 Report?

The OW-1 is a report that accounts for every gallon of wine on your bonded winery premises. Think of it as a strict reconciliation: wine in, wine out, and wine on hand. The TTB wants to know:

- How much wine you started the period with.
- How much you produced through crushing and fermenting.
- How much you received from other bonded premises.
- How much you removed for sale, transfer, or destruction.
- How much you have on hand at the end of the period.
- What additions you made and their specific TTB codes.

The report covers all wine, including fermenting wine in process, finished wine, and distilling material.

## Who Must File?

Every holder of a federal Basic Permit for a bonded winery must file the OW-1. Depending on your annual tax liability, the TTB will require you to file monthly, quarterly, or annually. Small boutique operations often qualify for quarterly or annual filing, while larger facilities file every single month. Regardless of your frequency, the data requirements are identical.

## What Data the OW-1 Requires

The form is divided into specific sections.

**Part I: Wine on Hand.** Your beginning inventory for the period, broken down by type and tax class.

**Part II: Wine Produced.** All wine produced during the period, including grapes crushed, juice obtained, and wine produced by fermentation.

**Part III: Wine Received.** Wine transferred in from other bonded premises or imported wine received.

**Part IV: Wine Removed.** Wine bottled and removed for sale, wine transferred to other bonded premises, wine destroyed, and tax-free removals.

**Part V: Additions and Cellar Treatment.** This is where most winemakers trip up. Every addition must be logged with the TTB-approved material code, the amount used, the purpose, and the specific lot it was applied to. Common additions include SO2, tartaric acid, and bentonite. Each has a specific TTB code. Using the wrong one creates a discrepancy in the TTB''s records that you will eventually have to explain.

**Part VI: Ending Inventory.** Your closing inventory, which automatically becomes your next opening inventory.

## Common Mistakes That Trigger Audits

**Beginning inventory does not match last period''s ending inventory.** This is the ultimate red flag. If your numbers do not carry forward correctly, the TTB will notice instantly.

**Missing or incorrect addition codes.** Using the wrong TTB code for a cellar treatment creates a glaring discrepancy in the records.

**Not accounting for losses.** Evaporation, racking losses, and ullage must be reported. Unexplained volume discrepancies are immediate audit triggers.

**Late filing.** Your report is due by the 15th day of the month following your reporting period. January''s report is due February 15th. There are no extensions. Late filings generate automatic notices.

**Inconsistent units.** The TTB demands gallons. If your internal records use liters, you must convert them accurately before filing.

